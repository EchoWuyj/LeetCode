    /*
        查找算法
        1 线性查找 -> O(n)
        2 二分查找(有序数组) -> O(logn)
        3 二叉查找树 -> O(logn)
        4 哈希查找 -> O(1)
            4.1 通过'哈希方法'计算出'查找元素'在数组中索引，'哈希方法'是数学公式，时间复杂度 O(1)
            4.2 通过索引随机访问数组元素，时间复杂度 O(1)

        刷题中常用
        1.HashSet：add，remove，contains -> O(1)
        2.HashMap：put(key,value)，get(key)，containsKey ->O(1)
        底层：数学公式，通过计算得到，所以时间复杂度 O(1)

        注意：若使用数组代替 HashSet 或者 HashMap，即使用元素作为数组下标，可以降低空间复杂度

     */
